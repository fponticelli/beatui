export type ThemeColor = keyof typeof textColors
export type ThemeColorShade = keyof (typeof textColors)[ThemeColor]
export type ThemedColor = ThemeColor | 'primary' | 'secondary' | 'neutral'

export function darkerShade(
  shade: ThemeColorShade,
  steps = 1
): ThemeColorShade {
  if (steps === 0) {
    return shade
  }
  if (steps < 0) {
    return lighterShade(shade, -steps)
  }
  if (steps > 1) {
    return darkerShade(darkerShade(shade, steps - 1), 1)
  }
  switch (shade) {
    case 50:
      return 100
    case 100:
      return 200
    case 200:
      return 300
    case 300:
      return 400
    case 400:
      return 500
    case 500:
      return 600
    case 600:
      return 700
    case 700:
      return 800
    case 800:
      return 900
    case 900:
      return 950
    case 950:
      return 950
  }
}

export function lighterShade(
  shade: ThemeColorShade,
  steps = 1
): ThemeColorShade {
  if (steps === 0) {
    return shade
  }
  if (steps < 0) {
    return darkerShade(shade, -steps)
  }
  if (steps > 1) {
    return lighterShade(lighterShade(shade, steps - 1), 1)
  }
  switch (shade) {
    case 50:
      return 50
    case 100:
      return 50
    case 200:
      return 100
    case 300:
      return 200
    case 400:
      return 300
    case 500:
      return 400
    case 600:
      return 500
    case 700:
      return 600
    case 800:
      return 700
    case 900:
      return 800
    case 950:
      return 900
  }
}

